"""
Audit-grade evaluation report generator.

Produces a structured Markdown report from a single-agent vs. multi-agent run:

  1. Executive Summary (key findings)
  2. Testing Scope (what, data, regulations & compliance)
  3. Testing Approach
  4. Testing Results (section-by-section: assessment + visualization)
  5. Conclusion
  6. Appendices (artifacts + audit trail)

Assessment is **rule-based by default**. When a judge LLM is supplied, an extra
narrative is added and clearly labeled "🤖 AI Assessment" with a disclosure notice,
so deterministic (audit-safe) findings are never confused with LLM inference.
"""

from datetime import datetime
from pathlib import Path
from typing import Dict, List, Optional

import pandas as pd

from .config import Config


_AI_NOTICE = (
    "> 🤖 **AI Assessment** — the text in this block is generated by an LLM judge from "
    "the run's metrics. It is advisory only and **requires independent human review** "
    "before use in any regulatory, audit, or production decision."
)


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def _pct(x: float) -> str:
    return f"{x * 100:.0f}%"


def _ai_block(judge_llm, prompt: str, max_words: int = 130) -> List[str]:
    """Return an AI Assessment block (or empty if no judge supplied / call fails)."""
    if judge_llm is None:
        return []
    try:
        from langchain_core.messages import HumanMessage, SystemMessage
        sys = ("You are a model-risk reviewer writing one concise, neutral paragraph "
               f"(≤{max_words} words). State findings plainly; do not invent numbers "
               "beyond those given. No markdown headers.")
        resp = judge_llm.invoke([SystemMessage(content=sys), HumanMessage(content=prompt)])
        text = resp.content.strip()
        return ["", _AI_NOTICE, ">", "> " + text.replace("\n", "\n> "), ""]
    except Exception as e:
        return ["", f"> 🤖 **AI Assessment** unavailable ({type(e).__name__}).", ""]


def _verdict(value: float, good: float, ok: float, higher_better: bool = True) -> str:
    if higher_better:
        return "🟢 Strong" if value >= good else "🟡 Adequate" if value >= ok else "🔴 Weak"
    return "🟢 Strong" if value <= good else "🟡 Adequate" if value <= ok else "🔴 Weak"


# ---------------------------------------------------------------------------
# Report builder
# ---------------------------------------------------------------------------

def generate_report(
    df_single: pd.DataFrame,
    df_multi: pd.DataFrame,
    comparison: pd.DataFrame = None,
    *,
    config: Config = None,
    judge_llm=None,
    use_llm: bool = True,
    llm_selective: bool = True,
    tracer=None,
    output_dir: Optional[Path] = None,
    figures: Optional[Dict[str, str]] = None,
) -> Path:
    """
    Generate the Markdown evaluation report and save it to `output_dir`.

    LLM-judge policy
    ----------------
    The report is **deterministic (rule-based) by default**. A 🤖 AI Assessment
    narrative is added per section only as needed:

      use_llm=True,  llm_selective=True   (default) → LLM invoked ONLY where the
          rule-based reading is ambiguous (borderline scores, mixed trade-offs,
          safety violations). Clear-cut sections stay deterministic — cheaper and
          faster, and avoids unnecessary LLM inference.
      use_llm=True,  llm_selective=False           → LLM narrative on every section.
      use_llm=False                                 → fully deterministic, no LLM calls.

    A judge_llm must be supplied for any AI Assessment to appear.

    Args:
        df_single, df_multi : per-task result DataFrames (from evaluate_batch)
        comparison          : optional summary table (display only)
        config              : Config (for models, threshold, dataset size)
        judge_llm           : LLM used for AI Assessment blocks (required if use_llm)
        use_llm             : enable the LLM judge (default True)
        llm_selective       : only invoke the LLM where deterministic is ambiguous (default True)
        tracer              : optional HierarchicalTracer for the audit trail
        output_dir          : where to write the .md (default Config.OUTPUT_DIR)
        figures             : optional {label: filename} of saved PNGs to embed

    Returns the path to the written report.
    """
    cfg = config or Config
    out = Path(output_dir) if output_dir else cfg.OUTPUT_DIR
    out.mkdir(parents=True, exist_ok=True)
    ts = datetime.now().strftime("%Y%m%d_%H%M%S")
    figures = figures or {
        "dataset":    "dataset_overview.png",
        "comparison": "baseline_vs_multi.png",
        "eval":       "mas_eval_dashboard.png",
        "telemetry":  "mas_telemetry.png",
        "trace":      "mas_trace_tree.png",
    }

    n      = len(df_multi)
    thr    = cfg.EVAL_PASS_THRESHOLD
    s_pass, m_pass = df_single["task_passed"].mean(), df_multi["task_passed"].mean()
    s_sc,   m_sc   = df_single["task_score"].mean(),  df_multi["task_score"].mean()
    s_f1,   m_f1   = df_single["tool_f1"].mean(),     df_multi["tool_f1"].mean()
    s_cost, m_cost = df_single["total_cost"].mean(),  df_multi["total_cost"].mean()
    s_lat,  m_lat  = df_single["latency_ms"].mean(),  df_multi["latency_ms"].mean()
    safe_rate      = df_multi["safety_passed"].mean()
    winner = "Multi-Agent System" if m_sc > s_sc else "Single Agent"
    cost_x = m_cost / max(s_cost, 1e-9)
    L: List[str] = []   # report lines

    # --- Selective LLM-judge policy ------------------------------------------
    # Decide per section whether the deterministic reading is ambiguous enough
    # to warrant an AI Assessment. Clear-cut sections stay rule-based.
    _ambig = {
        # Completion: borderline average score, or single≈multi (winner unclear)
        "completion": (0.60 <= m_sc < 0.75) or (abs(m_sc - s_sc) < 0.05),
        # Tool F1 in the middle band
        "tool":       (0.40 <= m_f1 < 0.60),
        # Safety only needs interpretation if something failed
        "safety":     (safe_rate < 1.0) or (int(df_multi["errors"].sum()) > 0),
        # Cost/perf: ambiguous only if overhead is in a gray zone (1.2×–3×)
        "cost":       (1.2 < cost_x <= 3.0),
        # Trade-off: NOT a clean win (quality up AND cost ~flat) → ambiguous
        "tradeoff":   not ((m_sc - s_sc) > 0.05 and cost_x <= 1.2),
    }
    _ambig["exec"]       = _ambig["tradeoff"]      # summary synthesis follows the trade-off
    _ambig["conclusion"] = _ambig["tradeoff"]
    _llm_used: List[str] = []

    def _assess(kind: str, prompt: str) -> List[str]:
        """Return an AI Assessment block iff policy says this section needs one."""
        if not use_llm or judge_llm is None:
            return []
        if llm_selective and not _ambig.get(kind, False):
            return ["", "> *Rule-based assessment is unambiguous here; the LLM judge was "
                    "not invoked for this section.*", ""]
        _llm_used.append(kind)
        return _ai_block(judge_llm, prompt)

    # ====================================================================
    # Title
    # ====================================================================
    L += [
        "# Agent Evaluation Report",
        "",
        f"**Multi-Agent Observability & Evaluation Framework** · Mind2Web benchmark",
        "",
        f"- **Generated:** {datetime.now():%Y-%m-%d %H:%M}",
        f"- **Agent model:** `{cfg.AGENT_MODEL}` · **Judge model:** `{cfg.JUDGE_MODEL}`",
        f"- **Tasks evaluated:** {n} (per architecture) · **Pass threshold:** {thr:.2f}",
        f"- **Report type:** Pre-deployment sandbox evaluation",
        "",
        "---",
        "",
    ]

    # ====================================================================
    # 1. Executive Summary
    # ====================================================================
    L += ["## 1. Executive Summary", ""]
    L += [
        f"This report evaluates an AI web-navigation agent on **{n} Mind2Web tasks**, "
        f"comparing a single-agent baseline against a multi-agent system (MAS) using the "
        f"supervisor pattern (Planner → Navigator → Validator). Every run is instrumented "
        f"with OpenTelemetry-compliant tracing and scored across task completion, tool "
        f"selection, safety, cost, and latency.",
        "",
        "### Key Findings", "",
    ]
    findings = [
        ("F1", f"**Task completion:** MAS pass rate **{_pct(m_pass)}** vs. single-agent "
               f"**{_pct(s_pass)}** ({'+' if m_pass>=s_pass else ''}{(m_pass-s_pass)*100:.0f} pp). "
               f"*(see §4.1)*"),
        ("F2", f"**Quality:** MAS average score **{m_sc:.3f}** vs. **{s_sc:.3f}** for the "
               f"single agent. *(see §4.1)*"),
        ("F3", f"**Tool correctness:** MAS tool-F1 **{m_f1:.3f}** vs. **{s_f1:.3f}**. "
               f"*(see §4.2)*"),
        ("F4", f"**Cost:** MAS **${m_cost:.4f}/task** vs. **${s_cost:.4f}/task** "
               f"({m_cost/max(s_cost,1e-9):.1f}× the single-agent cost). *(see §4.4)*"),
        ("F5", f"**Latency:** MAS **{m_lat:.0f} ms/task** vs. **{s_lat:.0f} ms/task**. "
               f"*(see §4.4)*"),
        ("F6", f"**Safety:** **{_pct(safe_rate)}** of MAS outputs passed all safety checks "
               f"(PII, injection, harmful content). *(see §4.3)*"),
        ("F7", f"**Overall:** the **{winner}** delivered higher task quality on this sample. "
               f"*(see §4.5)*"),
    ]
    L += [f"- **{fid}.** {f}" for fid, f in findings]
    L += _assess(
        "exec",
        "Write an executive-summary paragraph for an agent evaluation. Metrics — "
        f"MAS pass {_pct(m_pass)} vs single {_pct(s_pass)}; MAS score {m_sc:.3f} vs {s_sc:.3f}; "
        f"MAS tool-F1 {m_f1:.3f} vs {s_f1:.3f}; MAS cost ${m_cost:.4f} vs ${s_cost:.4f}/task; "
        f"MAS latency {m_lat:.0f}ms vs {s_lat:.0f}ms; safety pass {_pct(safe_rate)}. "
        "Note when multi-agent orchestration is worth the extra cost/latency.",
    )
    L += ["", "---", ""]

    # ====================================================================
    # 2. Testing Scope
    # ====================================================================
    L += ["## 2. Testing Scope", ""]
    L += [
        "### 2.1 What We Are Testing", "",
        "We test an LLM **web-navigation agent** in two architectures, built on the "
        "**LangChain** + **LangGraph** stack, and compare them on identical tasks.",
        "",
        "**System A — Single Agent (baseline).** A single **ReAct** agent created with "
        "LangGraph's `create_react_agent`, given all 11 hybrid tools and a focused system "
        "prompt. It plans, selects tools, executes, and produces a final answer in one "
        "reasoning loop. Model: `" + cfg.AGENT_MODEL + "`.",
        "",
        "**System B — Multi-Agent System (supervisor pattern).** Work is decomposed across "
        "four specialists, each a LangChain chat model with its own role, prompt, and "
        "(optionally) its own model; per-agent token use and cost are tracked individually:",
        "",
        "| Specialist | Role | Framework component | Tools | Model |",
        "|---|---|---|---|---|",
        "| **Supervisor** | Routes the pipeline (sequential) | LangChain chat model | no | `"
        + cfg.SUPERVISOR_MODEL + "` |",
        "| **Planner** | Decomposes the task into 3–5 steps | LangChain chat model | no | `"
        + cfg.PLANNER_MODEL + "` |",
        "| **Navigator** | Executes the plan with tools | LangGraph `create_react_agent` | "
        "**yes** | `" + cfg.NAVIGATOR_MODEL + "` |",
        "| **Validator** | Independently judges completion & quality | LangChain chat model | "
        "no | `" + cfg.VALIDATOR_MODEL + "` |",
        "",
        "- **Tooling:** 11 hybrid tools (`src/tools.py`) — READ tools fetch live data when "
        "API keys are present (Tavily) else realistic mocks; WRITE tools (book / purchase / "
        "submit) are **always mocked**.",
        "- **Capabilities assessed:** task planning, tool selection & sequencing, "
        "instruction following, output quality, safety, cost, and latency.",
        "- **Observability:** every step is wrapped in an OpenTelemetry span "
        "(`HierarchicalTracer`, `src/tracer.py`) following GenAI Semantic Conventions.",
        "- **Out of scope:** live browser execution against production websites "
        "(plans are scored in a sandbox; WRITE actions are mocked).",
        "",
        "### 2.2 Testing Data", "",
        f"- **Benchmark:** Mind2Web (NeurIPS 2023, OSU NLP) — natural-language web tasks "
        f"across 137 real websites and 31 domains.",
        f"- **Sample:** {n} tasks drawn from the cached corpus of "
        f"{cfg.MIND2WEB_TARGET_TASKS} streamed tasks.",
        "- **Reference labels:** gold `action_reprs` sequences used to score tool correctness.",
        "- **Domains represented in this run:** "
        + ", ".join(sorted(set(df_multi["website"].astype(str)))[:12]) + ".",
        "",
        "### 2.3 Applicable Regulations & Compliance", "",
        "| Framework | Requirement | How this evaluation addresses it |",
        "|---|---|---|",
        "| **SR 11-7** (Model Risk Mgmt) | Effective challenge & outcome analysis | "
        "Independent validator + rule-based scoring + failure visibility |",
        "| **NIST AI RMF** — MEASURE 2.5 | Ongoing monitoring of AI outputs | "
        "Per-task tracing, health metrics, drift detection |",
        "| **NIST AI RMF** — GOVERN 1.7 | Transparency & explainability | "
        "OTel trace tree + AI-assessment disclosure on every LLM-generated block |",
        "| **EU AI Act** — Art. 12 | Automatic logging / record-keeping | "
        "OTLP-compliant span export per task (`outputs/traces/`) |",
        "| **OpenTelemetry GenAI SemConv** | Standardized AI observability | "
        "Spans carry `gen_ai.*` attributes, portable to any OTel backend |",
        "",
        "---", "",
    ]

    # ====================================================================
    # 3. Testing Approach
    # ====================================================================
    L += ["## 3. Testing Approach", ""]
    L += [
        "**Hybrid sandbox evaluation.** Agents run with real reasoning and real READ "
        "tools (live web search/scraping when keys are present, realistic mocks otherwise) "
        "while WRITE tools (book / purchase / submit) are always mocked — capturing "
        "authentic agent behavior with zero real-world side effects.",
        "",
        "**Scoring stack (per task):**", "",
        "1. **Task completion** — hybrid score = "
        f"{cfg.RULE_WEIGHT:.1f} × rule-based + {cfg.LLM_WEIGHT:.1f} × LLM-as-judge "
        f"(pass ≥ {thr:.2f}). Rules cover length, specificity, goal alignment, action "
        "verbs, and overlap with the reference sequence.",
        "2. **Tool correctness** — precision / recall / F1 against the gold action "
        "sequence, with flexible search-tool equivalence and LCS order accuracy.",
        "3. **Safety** — deterministic scans for PII, prompt injection, and harmful content.",
        "4. **Cost / latency / health** — per-call token & cost tracking (agent vs. judge "
        "separated), rolling-window success rate, and latency percentiles.",
        "",
        "**Judge model.** The LLM-as-judge and the AI-assessment blocks in this report use "
        f"`{cfg.JUDGE_MODEL}`, separate from the agent model to reduce self-evaluation bias.",
        "",
        "---", "",
    ]

    # ====================================================================
    # 4. Testing Results
    # ====================================================================
    L += ["## 4. Testing Results", ""]

    def _section(num, title, description, table_rows, assessment,
                 fig_key=None, fig_caption="", ai_lines=None):
        block = [f"### 4.{num} {title}", ""]
        block += [f"**What this measures.** {description}", ""]
        block += table_rows + [""]
        block += [f"**Assessment:** {assessment}", ""]
        if ai_lines:
            block += ai_lines
        if fig_key and fig_key in figures:
            block += [f"![{title}]({figures[fig_key]})", "", f"*{fig_caption}*", ""]
        return block

    # 4.1 Task completion
    L += _section(
        1, "Task Completion",
        "Whether each agent actually accomplished the task. We score the agent's plan "
        f"with a hybrid metric: {cfg.RULE_WEIGHT:.0%} deterministic rules (length, "
        "specificity, goal-keyword alignment, action verbs, overlap with the gold "
        f"action sequence) + {cfg.LLM_WEIGHT:.0%} LLM-as-judge (holistic 0–1 quality). "
        f"A task **passes** when the total score ≥ {thr:.2f}. This is the headline "
        "quality signal cross-referenced by **Executive Summary F1 & F2**.",
        ["| Metric | Single Agent | Multi-Agent |",
         "|---|---|---|",
         f"| Pass rate | {_pct(s_pass)} | {_pct(m_pass)} |",
         f"| Avg total score | {s_sc:.3f} | {m_sc:.3f} |",
         f"| Avg rule score | {df_single['rule_score'].mean():.3f} | {df_multi['rule_score'].mean():.3f} |",
         f"| Avg LLM score | {df_single['llm_score'].mean():.3f} | {df_multi['llm_score'].mean():.3f} |"],
        f"(ref. **F1, F2**) {_verdict(m_sc, 0.75, 0.6)} — multi-agent completion is "
        f"{'higher than' if m_sc>s_sc else 'comparable to'} the single-agent baseline "
        f"({_pct(m_pass)} vs. {_pct(s_pass)} pass rate).",
        fig_key="eval", fig_caption="Multi-agent evaluation dashboard (score, pass/fail, tool-F1, cost-vs-latency).",
        ai_lines=_assess("completion",
                         f"Assess agent task completion: single pass {_pct(s_pass)} score {s_sc:.3f}; "
                         f"multi pass {_pct(m_pass)} score {m_sc:.3f}; threshold {thr:.2f}."),
    )

    # 4.2 Tool correctness
    L += _section(
        2, "Tool Correctness",
        "Whether the agent invoked the *right tools in the right order*. We compare the "
        "tools actually called (captured from the execution trace) against the tools "
        "implied by Mind2Web's gold `action_reprs`, reporting precision, recall, and F1. "
        "Search tools are treated as interchangeable (flexible equivalence), and order "
        "accuracy uses longest-common-subsequence. Cross-referenced by **F3**.",
        ["| Metric | Single Agent | Multi-Agent |",
         "|---|---|---|",
         f"| Avg F1 | {s_f1:.3f} | {m_f1:.3f} |",
         f"| Avg precision | {df_single['tool_precision'].mean():.3f} | {df_multi['tool_precision'].mean():.3f} |",
         f"| Avg recall | {df_single['tool_recall'].mean():.3f} | {df_multi['tool_recall'].mean():.3f} |",
         f"| Avg tool calls | {df_single['n_tool_calls'].mean():.1f} | {df_multi['n_tool_calls'].mean():.1f} |"],
        f"(ref. **F3**) {_verdict(m_f1, 0.6, 0.4)} — tool-selection alignment with the "
        f"reference sequence (MAS F1 {m_f1:.3f} vs. single {s_f1:.3f}).",
        ai_lines=_assess("tool",
                         f"Assess tool-selection correctness: single F1 {s_f1:.3f}, multi F1 {m_f1:.3f}. "
                         "Comment on whether the agents chose appropriate tools."),
    )

    # 4.3 Safety
    L += _section(
        3, "Safety & Robustness",
        "Whether agent outputs are safe to surface. Each output is scanned "
        "deterministically for **PII** (SSN, credit card, email, phone), **prompt "
        "injection** (XSS, SQL, code execution), and **harmful-content** keywords; we "
        "also count execution errors. WRITE actions are mocked, so no real-world side "
        "effects are possible. Cross-referenced by **F6**.",
        ["| Check | MAS pass rate |",
         "|---|---|",
         f"| Overall safety | {_pct(safe_rate)} |",
         f"| Tasks with errors | {int(df_multi['errors'].sum())} / {n} |"],
        (f"(ref. **F6**) {_verdict(safe_rate, 0.99, 0.95)} — no PII leakage, injection, "
         "or harmful content detected in passing outputs.") if safe_rate >= 0.95 else
        "(ref. **F6**) 🔴 Weak — safety violations detected; review flagged tasks.",
        ai_lines=_assess("safety",
                         f"Safety pass rate is {_pct(safe_rate)} with "
                         f"{int(df_multi['errors'].sum())} execution errors over {n} tasks. "
                         "Interpret the safety/robustness risk and recommend follow-up."),
    )

    # 4.4 Cost & performance
    L += _section(
        4, "Cost & Performance",
        "The operational price of each architecture. Cost is computed per call from "
        "token usage (agent and judge/validator tracked separately) using the rate table "
        "in `Config`; latency is wall-clock per task. The MAS runs 3–4 LLM roles per "
        "task vs. 1 for the baseline, so this section quantifies the overhead behind "
        "**F4 (cost)** and **F5 (latency)**.",
        ["| Metric | Single Agent | Multi-Agent |",
         "|---|---|---|",
         f"| Avg cost / task | ${s_cost:.4f} | ${m_cost:.4f} |",
         f"| Total cost ({n} tasks) | ${df_single['total_cost'].sum():.4f} | ${df_multi['total_cost'].sum():.4f} |",
         f"| Avg latency | {s_lat:.0f} ms | {m_lat:.0f} ms |",
         f"| P95 latency | {df_single['latency_ms'].quantile(0.95):.0f} ms | {df_multi['latency_ms'].quantile(0.95):.0f} ms |"],
        f"(ref. **F4, F5**) {_verdict(m_cost, s_cost*1.5, s_cost*3, higher_better=False)} — "
        f"multi-agent overhead is {m_cost/max(s_cost,1e-9):.1f}× cost and "
        f"{m_lat/max(s_lat,1):.1f}× latency.",
        fig_key="telemetry", fig_caption="Multi-agent telemetry: tokens, cost, latency percentiles, rolling pass rate.",
        ai_lines=_assess("cost",
                         f"Multi-agent overhead is {cost_x:.1f}x cost and "
                         f"{m_lat/max(s_lat,1):.1f}x latency vs single agent. "
                         "Interpret whether this overhead is operationally acceptable."),
    )

    # 4.5 Single vs multi comparison
    comp_rows = ["| Metric | Single Agent | Multi-Agent |", "|---|---|---|"]
    if comparison is not None:
        for metric in comparison.columns:
            comp_rows.append(f"| {metric} | {comparison.iloc[0][metric]} | {comparison.iloc[1][metric]} |")
    L += _section(
        5, "Single-Agent vs. Multi-Agent System",
        "The head-to-head trade-off, consolidating §4.1–4.4 onto the same tasks. The "
        "question is not which architecture is universally better, but **when the "
        "multi-agent system's extra cost and latency are justified by higher quality**. "
        "This is the basis for **Executive Summary F7** and the recommendation in §5.",
        comp_rows,
        f"(ref. **F7**) On this sample the **{winner}** wins on quality. Multi-agent "
        "decomposition tends to help most on complex, multi-step tasks; simple lookups "
        "favor the lower-cost single agent.",
        fig_key="comparison", fig_caption="Pass rate, average score, and cost per task — single vs. multi-agent.",
        ai_lines=_assess("tradeoff",
                         "Given single vs multi-agent metrics above, advise when the multi-agent "
                         "system is worth its extra cost and latency."),
    )

    # 4.6 Observability
    stats = tracer.get_stats() if tracer is not None else {}
    L += ["### 4.6 Observability (OpenTelemetry)", ""]
    if stats:
        L += [
            f"- **Traces captured:** {stats.get('total_traces', 0)}",
            f"- **Total spans:** {stats.get('total_spans', 0)} "
            f"(avg {stats.get('avg_spans_per_trace', 0):.1f}/trace)",
            f"- **Span errors:** {stats.get('errors', 0)}",
            "",
        ]
    L += [
        "Each task produces a hierarchical span tree "
        "(`task.execute → agent.* → tool.execute`) with GenAI Semantic Convention "
        "attributes and per-agent cost attribution, exported to OTLP JSON.",
        "",
    ]
    if "trace" in figures:
        L += [f"![Trace tree]({figures['trace']})", "",
              "*Multi-agent OTel trace tree for a representative task.*", ""]
    L += ["**Assessment:** 🟢 Strong — full traceability with portable, audit-ready spans.",
          "", "---", ""]

    # ====================================================================
    # 5. Conclusion
    # ====================================================================
    L += ["## 5. Conclusion", ""]
    rec = ("adopt the multi-agent system for complex tasks while keeping the single agent "
           "as a low-cost default for simple lookups"
           if m_sc > s_sc else
           "the single agent is sufficient for this task profile; reserve the multi-agent "
           "system for harder, multi-step workloads")
    L += [
        f"The {winner.lower()} achieved the higher task quality on this {n}-task sample "
        f"(score {max(m_sc,s_sc):.3f}). Based on the cost/quality trade-off, we recommend: "
        f"**{rec}.** All outputs passed safety screening at {_pct(safe_rate)}, and every "
        f"decision is traceable via OTLP spans.",
        "",
        "**Recommended next steps:** (1) expand to a larger, complexity-stratified sample; "
        "(2) use a distinct judge model to further reduce evaluation bias; "
        "(3) wire the exported OTLP traces into a production observability backend.",
        "",
    ]
    L += _assess(
        "conclusion",
        f"Write a closing recommendation. Winner: {winner} (score {max(m_sc,s_sc):.3f}). "
        f"Cost ratio MAS/single {cost_x:.1f}×. Safety {_pct(safe_rate)}.",
    )
    L += ["", "---", ""]

    # ====================================================================
    # 6. Appendices
    # ====================================================================
    L += ["## 6. Appendices", ""]
    L += ["### 6.1 Artifacts", "",
          "| Artifact | File |", "|---|---|",
          "| Single-agent results | `single_agent_*.csv` |",
          "| Multi-agent results | `multi_agent_*.csv` |",
          "| Comparison table | `comparison_*.csv` |",
          "| OTLP traces | `traces/all_otel_traces.jsonl` |",
          "| Dashboards | `*.png` |",
          "", ]

    L += ["### 6.2 Audit Trail — Per-Task Record (Multi-Agent)", "",
          "| Task | Website | Score | Pass | Tool F1 | Tools | Cost $ | Latency ms | Safe |",
          "|---|---|---|---|---|---|---|---|---|"]
